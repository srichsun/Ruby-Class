# 常用實用shortcut，成為專業Programmer必備！
cmd 1 切換視窗
cmd t 新增tab
cml l focus 到搜尋框
cmd w 關掉tab
cmd shift t 開上一個被關掉的tab
cmd tab 往右切換app
cmd ` 往左切換tab
cmd d 選一樣字串，cmd按住d一直按就一直選
cmd 左 跳到最左
cmd 右 跳到最右
cmd backtrace 刪除整行
cmd shift 右 選到最右邊
ctrl cmd  上下移動code
cmd / => 註解 or 反註解
